     public void load()
      {
          int a;
        try {
            
            pst = con.prepareStatement("select * from product");
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData rd = rs.getMetaData();
            a = rd.getColumnCount();
            df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while (rs.next())
            {
                ArrayList<String> rowValues = new ArrayList<>();
                    for (int i = 1; i <= a; i++) {
                         rowValues.add(rs.getString("id"));
                         rowValues.add(rs.getString("pname"));
                         rowValues.add(rs.getString("description"));
                         rowValues.add(rs.getString("barcode"));
                         rowValues.add(rs.getString("cprice"));
                         rowValues.add(rs.getString("rprice"));
                         rowValues.add(rs.getString("quantity"));
                         rowValues.add(rs.getString("rlevel"));
                    }
                        df.addRow(rowValues.toArray());

            }
                
            
        } catch (SQLException ex) {
            Logger.getLogger(Vendor.class.getName()).log(Level.SEVERE, null, ex);
        }
      }

