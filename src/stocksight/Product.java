    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         try {
             String pname = txtpname.getText();
             String des = txtdes.getText();
             String barcode = txtbarcode.getText();
             String costprice = txtcprice.getText();
             String retailprice = txtrprice.getText();
             String quantity = txtqty.getText();
             String rlevel = txtlevel.getText();
             
            pst = con.prepareStatement("insert into product(pname,description,barcode,cprice,rprice,quantity,rlevel)values(?,?,?,?,?,?,?)");
            pst.setString(1, pname);
            pst.setString(2, des);
            pst.setString(3, barcode); 
            pst.setString(4, costprice);
            pst.setString(5, retailprice);
            pst.setString(6, quantity);
            pst.setString(7, rlevel);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Product Added");
            
            txtpname.setText("");
            txtdes.setText("");
            txtbarcode.setText("");
            txtcprice.setText("");
            txtrprice.setText("");
            txtqty.setText("");
            txtlevel.setText("");
            
            txtpname.requestFocus();
            load();
            
        } catch (SQLException ex) {
            Logger.getLogger(Vendor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

